# =============================================================================
# ML CCP - Machine Learning Algorithm Comparison and Performance Evaluation
# Dataset: Hotel Booking Cancellations
# Target: Predict whether a booking will be Canceled or Not_Canceled
# =============================================================================

import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
import warnings
import time

from sklearn.model_selection import train_test_split
from sklearn.preprocessing import LabelEncoder, StandardScaler
from sklearn.linear_model import LogisticRegression
from sklearn.tree import DecisionTreeClassifier
from sklearn.svm import SVC
from sklearn.ensemble import RandomForestClassifier, GradientBoostingClassifier
from sklearn.metrics import (
    accuracy_score, precision_score, recall_score, f1_score,
    confusion_matrix, classification_report, roc_curve, auc
)

warnings.filterwarnings("ignore")
sns.set_theme(style="whitegrid")

# =============================================================================
# TASK 1: DATASET SELECTION AND UNDERSTANDING
# =============================================================================
print("=" * 60)
print("TASK 1: DATASET SELECTION AND UNDERSTANDING")
print("=" * 60)

# Load dataset
df = pd.read_csv("Hotel Booking Cancellations.csv")

print(f"\nSource       : Kaggle - Hotel Booking Cancellations Dataset")
print(f"Rows         : {df.shape[0]}")
print(f"Columns      : {df.shape[1]}")
print(f"\nFeatures and Data Types:")
print(df.dtypes)
print(f"\nTarget Variable : 'booking status'")
print(f"\nClass Distribution:")
print(df["booking status"].value_counts())
print(f"\nMissing Values per Column:")
print(df.isnull().sum())
print(f"\nDuplicate Rows : {df.duplicated().sum()}")
print(f"\nFirst 5 Rows:")
print(df.head())

# =============================================================================
# TASK 2: DATA PREPROCESSING
# =============================================================================
print("\n" + "=" * 60)
print("TASK 2: DATA PREPROCESSING")
print("=" * 60)

# Drop Booking_ID (not a feature, just an identifier)
df.drop(columns=["Booking_ID"], inplace=True)
print("\nDropped 'Booking_ID' column (non-informative identifier).")

# Drop 'date of reservation' - not useful as raw string without parsing
df.drop(columns=["date of reservation"], inplace=True)
print("Dropped 'date of reservation' column (raw string with date info not needed for this model).")

# No missing values found (confirmed in Task 1), so no imputation needed
print("No missing values found — no imputation required.")

# No duplicates found
print("No duplicate rows found — no removal required.")

# Encode categorical variables
le = LabelEncoder()
categorical_cols = ["type of meal", "room type", "market segment type"]
for col in categorical_cols:
    df[col] = le.fit_transform(df[col])
    print(f"Label Encoded: '{col}'")

# Encode target variable
df["booking status"] = le.fit_transform(df["booking status"])
# Not_Canceled = 1, Canceled = 0 (alphabetical order by LabelEncoder)
print("Label Encoded target: 'booking status' -> Canceled=0, Not_Canceled=1")

# Separate features and target
X = df.drop(columns=["booking status"])
y = df["booking status"]

# Scale numerical features (required for Logistic Regression and SVM)
numerical_cols = ["number of adults", "number of children", "number of weekend nights",
                  "number of week nights", "lead time", "P-C", "P-not-C",
                  "average price ", "special requests"]

scaler = StandardScaler()
X_scaled = X.copy()
X_scaled[numerical_cols] = scaler.fit_transform(X[numerical_cols])
print("\nApplied StandardScaler to numerical features (for LR and SVM).")

# Train-test split (80:20)
X_train, X_test, y_train, y_test = train_test_split(
    X_scaled, y, test_size=0.2, random_state=42, stratify=y
)
print(f"\nTrain-Test Split: 80% Train ({len(X_train)} rows), 20% Test ({len(X_test)} rows)")
print(f"Stratified split to preserve class balance.")

# =============================================================================
# TASK 3: EXPLORATORY DATA ANALYSIS (EDA)
# =============================================================================
print("\n" + "=" * 60)
print("TASK 3: EXPLORATORY DATA ANALYSIS")
print("=" * 60)

fig, axes = plt.subplots(2, 3, figsize=(18, 10))
fig.suptitle("Exploratory Data Analysis - Hotel Booking Cancellations", fontsize=16, fontweight="bold")

# --- Plot 1: Class Distribution ---
status_counts = df["booking status"].value_counts()
axes[0, 0].bar(["Not_Canceled (1)", "Canceled (0)"], status_counts.values,
               color=["steelblue", "tomato"], edgecolor="black")
axes[0, 0].set_title("Class Distribution")
axes[0, 0].set_xlabel("Booking Status")
axes[0, 0].set_ylabel("Count")
for i, v in enumerate(status_counts.values):
    axes[0, 0].text(i, v + 100, str(v), ha="center", fontweight="bold")
print("\nPlot 1: Class Distribution - Not_Canceled dominates (~67%), Canceled ~33%.")

# --- Plot 2: Correlation Heatmap ---
corr = df.corr(numeric_only=True)
sns.heatmap(corr, ax=axes[0, 1], cmap="coolwarm", annot=False, linewidths=0.5)
axes[0, 1].set_title("Correlation Heatmap")
print("Plot 2: Correlation Heatmap - 'lead time' and 'P-not-C' show notable correlation with cancellation.")

# --- Plot 3: Lead Time Distribution ---
axes[0, 2].hist(df["lead time"], bins=40, color="mediumpurple", edgecolor="black", alpha=0.8)
axes[0, 2].set_title("Lead Time Distribution")
axes[0, 2].set_xlabel("Lead Time (Days)")
axes[0, 2].set_ylabel("Count")
print("Plot 3: Lead Time Distribution - Most bookings are made within 100 days; long lead times often cancel.")

# --- Plot 4: Average Price Distribution ---
axes[1, 0].hist(df["average price "], bins=40, color="goldenrod", edgecolor="black", alpha=0.8)
axes[1, 0].set_title("Average Room Price Distribution")
axes[1, 0].set_xlabel("Average Price")
axes[1, 0].set_ylabel("Count")
print("Plot 4: Average Price Distribution - Prices are right-skewed; most bookings are budget range.")

# --- Plot 5: Lead Time vs Booking Status ---
canceled = df[df["booking status"] == 0]["lead time"]
not_canceled = df[df["booking status"] == 1]["lead time"]
axes[1, 1].boxplot([not_canceled, canceled], labels=["Not_Canceled", "Canceled"],
                   patch_artist=True,
                   boxprops=dict(facecolor="lightblue"),
                   medianprops=dict(color="red", linewidth=2))
axes[1, 1].set_title("Lead Time vs Booking Status")
axes[1, 1].set_xlabel("Booking Status")
axes[1, 1].set_ylabel("Lead Time (Days)")
print("Plot 5: Canceled bookings tend to have much higher lead times than non-canceled ones.")

# --- Plot 6: Special Requests vs Booking Status ---
sr_status = df.groupby(["special requests", "booking status"]).size().unstack(fill_value=0)
sr_status.plot(kind="bar", ax=axes[1, 2], color=["tomato", "steelblue"], edgecolor="black")
axes[1, 2].set_title("Special Requests vs Booking Status")
axes[1, 2].set_xlabel("Number of Special Requests")
axes[1, 2].set_ylabel("Count")
axes[1, 2].legend(["Canceled", "Not_Canceled"])
axes[1, 2].tick_params(axis="x", rotation=0)
print("Plot 6: Bookings with 0 special requests are more likely to be canceled.")

plt.tight_layout()
plt.savefig("eda_plots.png", dpi=150, bbox_inches="tight")
plt.show()
print("\nEDA plots saved as 'eda_plots.png'")

# =============================================================================
# TASK 4 & 5: MODEL TRAINING AND EVALUATION
# =============================================================================
print("\n" + "=" * 60)
print("TASK 4 & 5: MODEL TRAINING AND EVALUATION")
print("=" * 60)

models = {
    "Logistic Regression": LogisticRegression(max_iter=1000, random_state=42),
    "Decision Tree": DecisionTreeClassifier(random_state=42),
    "SVM": SVC(kernel="rbf", probability=True, random_state=42),
    "Random Forest": RandomForestClassifier(n_estimators=100, random_state=42),
    "Gradient Boosting": GradientBoostingClassifier(n_estimators=100, random_state=42),
}

results = {}
roc_data = {}

for model_name, model in models.items():
    print(f"\n--- {model_name} ---")

    # Train
    start = time.time()
    model.fit(X_train, y_train)
    train_time = round(time.time() - start, 4)

    # Predict
    y_pred = model.predict(X_test)
    y_prob = model.predict_proba(X_test)[:, 1]

    # Metrics
    acc = accuracy_score(y_test, y_pred)
    prec = precision_score(y_test, y_pred)
    rec = recall_score(y_test, y_pred)
    f1 = f1_score(y_test, y_pred)
    cm = confusion_matrix(y_test, y_pred)

    results[model_name] = {
        "Accuracy": round(acc, 4),
        "Precision": round(prec, 4),
        "Recall": round(rec, 4),
        "F1-Score": round(f1, 4),
        "Training Time (s)": train_time,
    }

    # ROC
    fpr, tpr, _ = roc_curve(y_test, y_prob)
    roc_auc = auc(fpr, tpr)
    roc_data[model_name] = (fpr, tpr, roc_auc)

    print(f"  Accuracy      : {acc:.4f}")
    print(f"  Precision     : {prec:.4f}")
    print(f"  Recall        : {rec:.4f}")
    print(f"  F1-Score      : {f1:.4f}")
    print(f"  Training Time : {train_time}s")
    print(f"\n  Classification Report:")
    print(classification_report(y_test, y_pred, target_names=["Canceled", "Not_Canceled"]))
    print(f"  Confusion Matrix:")
    print(cm)

# =============================================================================
# CONFUSION MATRIX PLOTS
# =============================================================================
fig, axes = plt.subplots(2, 3, figsize=(18, 10))
fig.suptitle("Confusion Matrices - All Models", fontsize=16, fontweight="bold")
axes = axes.flatten()

for idx, (model_name, model) in enumerate(models.items()):
    y_pred = model.predict(X_test)
    cm = confusion_matrix(y_test, y_pred)
    sns.heatmap(cm, annot=True, fmt="d", cmap="Blues", ax=axes[idx],
                xticklabels=["Canceled", "Not_Canceled"],
                yticklabels=["Canceled", "Not_Canceled"])
    axes[idx].set_title(model_name)
    axes[idx].set_xlabel("Predicted")
    axes[idx].set_ylabel("Actual")

axes[5].axis("off")
plt.tight_layout()
plt.savefig("confusion_matrices.png", dpi=150, bbox_inches="tight")
plt.show()
print("\nConfusion matrix plots saved as 'confusion_matrices.png'")

# =============================================================================
# ROC CURVE PLOT
# =============================================================================
plt.figure(figsize=(10, 7))
colors = ["blue", "green", "red", "purple", "orange"]
for (model_name, (fpr, tpr, roc_auc)), color in zip(roc_data.items(), colors):
    plt.plot(fpr, tpr, color=color, lw=2,
             label=f"{model_name} (AUC = {roc_auc:.3f})")
plt.plot([0, 1], [0, 1], "k--", lw=1.5, label="Random Classifier")
plt.xlabel("False Positive Rate")
plt.ylabel("True Positive Rate")
plt.title("ROC Curves - All Models")
plt.legend(loc="lower right")
plt.tight_layout()
plt.savefig("roc_curves.png", dpi=150, bbox_inches="tight")
plt.show()
print("ROC curve plot saved as 'roc_curves.png'")

# =============================================================================
# FINAL COMPARISON TABLE
# =============================================================================
print("\n" + "=" * 60)
print("FINAL MODEL COMPARISON TABLE")
print("=" * 60)

results_df = pd.DataFrame(results).T
results_df.index.name = "Model"

# Add AUC column
results_df["AUC Score"] = [round(roc_data[m][2], 4) for m in results_df.index]

print(results_df.to_string())

# Summary remarks
remarks = {
    "Logistic Regression": "Good baseline; fast; needs scaling; lower performance on complex patterns.",
    "Decision Tree": "Highly interpretable; prone to overfitting without pruning.",
    "SVM": "Strong performance; computationally expensive; kernel=RBF used.",
    "Random Forest": "Robust ensemble; handles class imbalance well; good overall.",
    "Gradient Boosting": "Highest accuracy expected; slower to train; best for deployment.",
}
results_df["Remarks"] = [remarks[m] for m in results_df.index]
print("\nWith Remarks:")
print(results_df[["Accuracy", "Precision", "Recall", "F1-Score", "AUC Score", "Training Time (s)", "Remarks"]].to_string())

# =============================================================================
# COMPARISON BAR CHART
# =============================================================================
metrics_to_plot = ["Accuracy", "Precision", "Recall", "F1-Score"]
plot_df = results_df[metrics_to_plot].astype(float)

plot_df.plot(kind="bar", figsize=(13, 6), colormap="Set2", edgecolor="black")
plt.title("Model Performance Comparison")
plt.xlabel("Model")
plt.ylabel("Score")
plt.xticks(rotation=25, ha="right")
plt.ylim(0.5, 1.0)
plt.legend(loc="lower right")
plt.tight_layout()
plt.savefig("model_comparison.png", dpi=150, bbox_inches="tight")
plt.show()
print("\nModel comparison chart saved as 'model_comparison.png'")

# =============================================================================
# CONCLUSIONS AND DISCUSSION
# =============================================================================
print("\n" + "=" * 60)
print("CONCLUSIONS AND DISCUSSION")
print("=" * 60)

best_model = results_df["F1-Score"].idxmax()
worst_model = results_df["F1-Score"].idxmin()
slowest_model = results_df["Training Time (s)"].idxmax()

print(f"\nBest Performing Model       : {best_model}")
print(f"  -> Highest F1-Score and Accuracy overall.")
print(f"\nWorst Performing Model      : {worst_model}")
print(f"  -> Lowest F1-Score; simpler linear model struggles with non-linear patterns.")
print(f"\nMost Interpretable Model    : Decision Tree")
print(f"  -> Visual tree structure; easy to explain each decision rule.")
print(f"\nMost Computationally Expensive : {slowest_model}")
print(f"  -> Longest training time due to iterative boosting / kernel computation.")
print(f"\nRecommended for Deployment  : {best_model}")
print(f"  -> Best balance of accuracy, precision, recall, and F1-score.")

print("\n" + "=" * 60)
print("SCRIPT COMPLETE. Check the saved PNG files for all plots.")
print("=" * 60)

